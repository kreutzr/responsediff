package com.github.kreutzr.responsediff.proxy;

import java.util.List;

import com.github.kreutzr.responsediff.XmlHeader;

/**
 * This holds all configuration parameters.
 */
public class ProxyConfiguration 
{
  private int port_;
  private String targetBaseUrl_;
  private String storagePath_;
  private int maxThreads_;
  private List< String > basePathsToConsider_; 
  private List< String > basePathsToIgnore_;
  private List< XmlHeader > defaultHeaders_;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  public ProxyConfiguration()
  {
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  public ProxyConfiguration(
    final int               port,
    final String            targetBaseUrl,
    final String            storagePath,
    final int               maxThreads,
    final List< String >    basePathsToConsider, 
    final List< String >    basePathsToIgnore,
    final List< XmlHeader > defaultHeaders
  )
  {
    port_                = port;
    targetBaseUrl_       = targetBaseUrl;
    storagePath_         = storagePath;
    maxThreads_          = maxThreads;
    basePathsToConsider_ = basePathsToConsider; 
    basePathsToIgnore_   = basePathsToIgnore;
    defaultHeaders_      = defaultHeaders;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public int getPort() {
	return port_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setPort( final int port ) {
	port_ = port;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getTargetBaseUrl() {
	return targetBaseUrl_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setTargetBaseUrl( final String targetBaseUrl ) {
	targetBaseUrl_ = targetBaseUrl;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getStoragePath() {
	return storagePath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setStoragePath( final String storagePath ) {
	storagePath_ = storagePath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public int getMaxThreads() {
	return maxThreads_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setMaxThreads( final int maxThreads ) {
	maxThreads_ = maxThreads;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public List<String> getBasePathsToConsider() {
	return basePathsToConsider_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setBasePathsToConsider( final List<String> basePathsToConsider ) {
	basePathsToConsider_ = basePathsToConsider;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public List<String> getBasePathsToIgnore() {
	return basePathsToIgnore_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setBasePathsToIgnore( final List<String> basePathsToIgnore ) {
	basePathsToIgnore_ = basePathsToIgnore;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public List< XmlHeader > getDefaultHeaders() {
	return defaultHeaders_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setDefaultHeaders( final List< XmlHeader > defaultHeaders ) {
	defaultHeaders_ = defaultHeaders;
  }
}
