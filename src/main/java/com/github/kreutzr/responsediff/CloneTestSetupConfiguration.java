package com.github.kreutzr.responsediff;

/**
 * Invocation configuration for CloneTestSetup main class.
 */
public class CloneTestSetupConfiguration
{
  private String  rootPath_;
  private String  sourceFilePath_;
  private String  targetFolderPath_;
  private boolean overwriteTarget_;
  private boolean disableIgnoreTags_;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getRootPath()
  {
    return rootPath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setRootPath( final String rootPath)
  {
    rootPath_ = rootPath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getSourceFilePath()
  {
    return sourceFilePath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setSourceFilePath( final String sourceFilePath )
  {
    sourceFilePath_ = sourceFilePath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getTargetFolderPath()
  {
    return targetFolderPath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setTargetFolderPath( final String targetFolderPath )
  {
    targetFolderPath_ = targetFolderPath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public boolean getOverwriteTarget()
  {
    return overwriteTarget_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setOverwriteTarget( final boolean overwriteTarget )
  {
    overwriteTarget_ = overwriteTarget;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public boolean getDisableIgnoreTags()
  {
    return disableIgnoreTags_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setDisableIgnoreTags( final boolean disableIgnoreTags )
  {
    disableIgnoreTags_ = disableIgnoreTags;
  }
}
