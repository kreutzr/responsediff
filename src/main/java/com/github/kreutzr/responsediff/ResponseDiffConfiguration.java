package com.github.kreutzr.responsediff;

import java.util.List;

/**
 * Invocation configuration for ResponseDiff main class.
 */
public class ResponseDiffConfiguration
{
  private String            rootPath_;
  private String            xmlFilePath_;
  private String            testIdPattern_;
  private String            xsltFilePath_;
  private String            reportFileEnding_;
  private String            reportConversionFormats_;
  private String            storeResultPath_;
  private boolean           reportWhiteNoise_;
  private String            ticketServiceUrl_;
  private String            candidateServiceUrl_;
  private List< XmlHeader > candidateHeaders_;
  private String            referenceServiceUrl_;
  private List< XmlHeader > referenceHeaders_;
  private String            controlServiceUrl_;
  private List< XmlHeader > controlHeaders_;
  private long              responseTimeoutMs_ = 1000;
  private double            epsilon_ = 0.00000001;
  private String            referenceFilePath_;
  private boolean           exitWithExitCode_ = true;
  private long              startupSleepMs_ = -1;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getRootPath()
  {
    return rootPath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setRootPath( final String rootPath )
  {
    rootPath_ = rootPath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getXmlFilePath()
  {
    return xmlFilePath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setXmlFilePath( final String xmlFilePath )
  {
    xmlFilePath_ = xmlFilePath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getTestIdPattern()
  {
    return testIdPattern_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setTestIdPattern( final String testIdPattern )
  {
    testIdPattern_ = testIdPattern;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getXsltFilePath()
  {
    return xsltFilePath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setXsltFilePath( final String xsltFilePath )
  {
    xsltFilePath_ = xsltFilePath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getReportFileEnding()
  {
    return reportFileEnding_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setReportFileEnding( final String reportFileEnding )
  {
    reportFileEnding_ = reportFileEnding;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getReportConversionFormats()
  {
    return reportConversionFormats_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setReportConversionFormats( final String reportConversionFormats )
  {
    reportConversionFormats_ = reportConversionFormats;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getStoreResultPath()
  {
    return storeResultPath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setStoreResultPath( final String storeResultPath )
  {
    storeResultPath_ = storeResultPath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public boolean getReportWhiteNoise()
  {
    return reportWhiteNoise_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setReportWhiteNoise( final boolean reportWhiteNoise )
  {
    reportWhiteNoise_ = reportWhiteNoise;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getTicketServiceUrl()
  {
    return ticketServiceUrl_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setTicketServiceUrl( final String ticketServiceUrl )
  {
    ticketServiceUrl_ = ticketServiceUrl;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getCandidateServiceUrl()
  {
    return candidateServiceUrl_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setCandidateServiceUrl( final String candidateServiceUrl )
  {
    candidateServiceUrl_ = candidateServiceUrl;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public List< XmlHeader > getCandidateHeaders()
  {
    return candidateHeaders_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setCandidateHeaders( final List< XmlHeader > candidateHeaders )
  {
    candidateHeaders_ = trim( candidateHeaders );
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getReferenceServiceUrl()
  {
    return referenceServiceUrl_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setReferenceServiceUrl( final String referenceServiceUrl )
  {
    referenceServiceUrl_ = referenceServiceUrl;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public List< XmlHeader > getReferenceHeaders()
  {
    return referenceHeaders_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setReferenceHeaders( final List< XmlHeader > referenceHeaders )
  {
    referenceHeaders_ = trim( referenceHeaders );
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getControlServiceUrl()
  {
    return controlServiceUrl_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setControlServiceUrl( final String controlServiceUrl )
  {
    controlServiceUrl_ = controlServiceUrl;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public List< XmlHeader > getControlHeaders()
  {
    return controlHeaders_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setControlHeaders( final List< XmlHeader > controlHeaders )
  {
    controlHeaders_ = trim( controlHeaders );
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public long getResponseTimeoutMs()
  {
    return responseTimeoutMs_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setResponseTimeoutMs( final long responseTimeoutMs )
  {
    responseTimeoutMs_ = responseTimeoutMs;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public double getEpsilon()
  {
    return epsilon_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setEpsilon( final double epsilon )
  {
    epsilon_ = epsilon;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getReferenceFilePath()
  {
    return referenceFilePath_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setReferenceFilePath( final String referenceFilePath )
  {
    referenceFilePath_ = referenceFilePath;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public boolean isExitWithExitCode()
  {
    return exitWithExitCode_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setExitWithExitCode( final boolean exitWithExitCode )
  {
    exitWithExitCode_ = exitWithExitCode;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public long getStartupSleepMs()
  {
    return startupSleepMs_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public void setStartupSleepMs( final long startupSleepMs )
  {
    startupSleepMs_ = startupSleepMs;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  private List< XmlHeader > trim( final List< XmlHeader > xmlHeaders )
  {
    for( final XmlHeader xmlHeader : xmlHeaders ) {
      xmlHeader.setName( xmlHeader.getName().trim() );
    }
    return xmlHeaders;
  }
}
