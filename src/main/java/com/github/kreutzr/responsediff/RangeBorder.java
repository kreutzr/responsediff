package com.github.kreutzr.responsediff;

/**
 * Helper class to handle value range borders
 */
public class RangeBorder
{
  final RangeType type_;
  final String value_;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public RangeBorder(
    final RangeType type,
    final String value
  )
  {
    type_ = type;
    value_ = value;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public RangeType getType()
  {
    return type_;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String getValue()
  {
    return value_;
  }
}
